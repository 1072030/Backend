$(document).ready(function(){
    $("#regis-1").mouseover(function(){
        $("#regis").css("background-color","rgb(248,248,255)");
    })

    $("#regis-1").mouseout(function(){
        $("#regis").css("background-color","white");
    })

    $("#regis-2").mouseover(function(){
        $("#regis-2").css("background-color","rgb(248,248,255)");
    })
    $("#regis-2").mouseout(function(){
        $("#regis-2").css("background-color","white");
    })
})

